function choosePoem() {
  const allText = normalize(userAnswers.join(" "));
  const words = allText.split(/\s+/);

  let bestPoem = poems[0];
  let bestScore = -1;

  poems.forEach(poem => {
    const score = poem.keywords.reduce((acc, kw) => {
      return acc + (words.includes(normalize(kw)) ? 1 : 0);
    }, 0);
    if (score > bestScore) {
      bestScore = score;
      bestPoem = poem;
    }
  });

  if (bestScore === 0) {
    bestPoem = poems[Math.floor(Math.random() * poems.length)];
  }

  const color = getCurrentColor();
  addMessage("ai", "Creo que este poema puede acompañar lo que escribiste.");

  const card = document.createElement("div");
  card.className = "poem-card";
  card.style.color = "rgb(0, 0, 0)";
  card.style.borderLeftColor = "rgb(0, 0, 0)";
  card.innerHTML = `
    <div class="poem-title" style="color:rgb(0,0,0)">${bestPoem.title}</div>
    <div class="poem-text">${bestPoem.text}</div>
    <img src="${bestPoem.image}" alt="" style="display:block; width:100%; max-width:320px; margin:24px auto 0; opacity:0.85;">
  `;
  chat.appendChild(card);
  window.scrollTo(0, document.body.scrollHeight);

  setTimeout(showPostPoem, 800);
}

function resetChat() {
  step = 0;
  colorIndex = 0;
  userAnswers.length = 0;
  const oldMessages = chat.querySelectorAll(".message, .poem-card");
  oldMessages.forEach(el => el.remove());
  addMessage("ai", questions[0]);
  step++;
}

function sendMessage() {
  const text = input.value.trim();
  if (text === "") return;

  if (step === 1 && totalAttempts >= 5) {
    const haiku = checkSecretTrigger(text);
    if (haiku) {
      addMessage("user", text);
      input.value = "";
      setTimeout(() => showSecretHaiku(haiku), 600);
      return;
    }
  }

  addMessage("user", text);
  userAnswers.push(text);
  input.value = "";

  setTimeout(() => {
    if (step < questions.length) {
      addMessage("ai", questions[step]);
      step++;
    } else {
      totalAttempts++;
      choosePoem();
    }
  }, 600);
}

