function downloadPoemario() {
  const { jsPDF } = window.jspdf;
  const doc = new jsPDF({
    unit: "mm",
    format: "letter"
  });

  const margen = 20;
  const anchoMax = 170;
  const tamNormal = 11;
  const tamTitulo = 13;
  const lineaAltura = 7;
  let y = 30;

  doc.setFont("times", "italic");
  doc.setFontSize(20);
  doc.text("Sal e idas, sal y heridas", 105, y, { align: "center" });
  y += 10;

  doc.setFontSize(12);
  doc.setFont("times", "normal");
  doc.text("por Lucero Parra", 105, y, { align: "center" });
  y += 25;

  const lineas = fullPoemario.split("\n");

  lineas.forEach(linea => {
    if (y > 270) {
      doc.addPage();
      y = 20;
    }

    const esSeparador = linea.startsWith("————");
    const esTituloSeccion = linea === linea.toUpperCase() && linea.trim().length > 0 && !esSeparador;

    if (esSeparador) {
      y += 4;
      return;
    }

    if (esTituloSeccion) {
      doc.setFont("times", "bold");
      doc.setFontSize(tamTitulo);
    } else {
      doc.setFont("times", "normal");
      doc.setFontSize(tamNormal);
    }

    if (linea.trim() === "") {
      y += lineaAltura * 0.6;
      return;
    }

    const partes = doc.splitTextToSize(linea, anchoMax);
    partes.forEach(parte => {
      if (y > 270) {
        doc.addPage();
        y = 20;
      }
      doc.text(parte, margen, y);
      y += lineaAltura;
    });
  });

  doc.save("sal-e-idas-sal-y-heridas.pdf");
}

